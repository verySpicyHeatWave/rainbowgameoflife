WIDTHSTORE = {1200, 1500, 1800, 2000}
windex = #WIDTHSTORE
WINDOW_WIDTH = WIDTHSTORE[windex]
WINDOW_HEIGHT = (WINDOW_WIDTH / 5) * 3

CELL_SIZE = WINDOW_WIDTH / 100
COLS = WINDOW_WIDTH / CELL_SIZE
ROWS = WINDOW_HEIGHT / CELL_SIZE
cellCount = ROWS * COLS
grid = {}
newState = {}
icolor = 1
myTimer = 0
redG = 0
greenG = 0
blueG = 0
youWon = false;
winTimer = 0;
statusCount = 0;



function love.load()    

    updateWindow() 
    love.window.setTitle('Rainbow Game of Life!')
    math.randomseed(os.time())

    --              The color list is derived from 24-bit values. Bits 0-7 are blue, 8-15 are green, and 16-23 are red.
    colorlist = {   16711680,   16744448,   16776960,   8453888,    65280,      65408,      65535,      33023,      255,        8388863,    16711935,   16711808}
    --              red         orange      yellow      litegreen   green       teal        aqua        indigo      blue        purple      magenta     pink

    setColorWith24BitVal(colorlist[icolor])

    for i=1, cellCount, 1 do
        xIndex  = (i - 1) % COLS
        yIndex  = ((i - 1 - xIndex) / ROWS) * WINDOW_HEIGHT / WINDOW_WIDTH
        grid[i] = Cell(math.floor(math.random() + 0.5), redG, greenG, blueG, xIndex, yIndex)
    end
end



function love.update(dt) 
    myTimer = myTimer + dt
    if myTimer < .0625 then
        do return end
    end    
    myTimer = 0
    
    if youWon then
        displayWinMessage()
        checkForResetKeyPress()
        do return end
    end
    
    incrementgColor(1)
    checkRules()
    checkForLeftClick()
    checkForResetKeyPress()

    for i=1, cellCount, 1 do
        if newState[i] ~= grid[i].state then
            if newState[i] == 1 then
                grid[i]:revive( redG,  greenG, blueG )
            elseif newState[i] == 0 then
                grid[i]:die()
            else
                grid[i]:die()
            end
        end
    end
    checkForWindowResize()
end



function love.draw()
    for i=1, cellCount, 1 do
        if grid[i].state == 1 then
            love.graphics.setColor(love.math.colorFromBytes(grid[i].red, grid[i].green, grid[i].blue, 255))
            love.graphics.rectangle('fill', (grid[i].x * CELL_SIZE), (grid[i].y * CELL_SIZE), CELL_SIZE - 1, CELL_SIZE - 1)
        end
    end
end



function checkneighbors(i, x, y)
    sum = 0
    for i=x-1, x+1, 1 do
        for j=y-1, y+1, 1 do
            c = (i + COLS) % COLS
            r = (j + ROWS) % ROWS
            sum = sum + grid[(c + (r * COLS)) + 1].state
        end
    end
    sum = sum - grid[i].state
    return sum
end



function setColorWith24BitVal(sum)
    if sum == nil then
        return
    end
    tsum = sum
    blueG = tsum % 256

    tsum = (tsum - blueG) / 256
    greenG = tsum % (256)

    redG = (tsum - greenG) / 256
end



function checkRules()    
    statusCount = 0
    for i=1, cellCount, 1 do
        local neighbors = checkneighbors(i, grid[i].x, grid[i].y)

        if grid[i].state == 1 then
            if neighbors < 2 or neighbors > 3 then
                newState[i] = 0
            end
        elseif grid[i].state == 0 and sum == 3 then
            newState[i] = 1
        else
            newState[i] = grid[i].state
        end
        if newState[i] == 0 then
            statusCount = statusCount + 1
        end
        if statusCount == cellCount then
            youWon = true
        end
    end
end



function checkForWindowResize()
    if love.keyboard.isDown('w') then
        windex = (windex % #WIDTHSTORE) + 1
        updateWindowValues()
        updateWindow()
    end

end



function updateWindowValues()
    WINDOW_WIDTH = WIDTHSTORE[windex]
    WINDOW_HEIGHT = (WINDOW_WIDTH / 5) * 3
    CELL_SIZE = WINDOW_WIDTH / 100
end



function updateWindow()
    push:setupScreen(WINDOW_WIDTH, WINDOW_HEIGHT, WINDOW_WIDTH, WINDOW_HEIGHT, {
        vsync = true,
        fullscreen = false,
        resizable = true
    })
end



function checkForLeftClick()
    if love.mouse.isDown({0, 1, 2}) then
        mouseX = love.mouse.getX()
        mouseY = love.mouse.getY()
        mouseX = (mouseX - (mouseX % CELL_SIZE)) / CELL_SIZE + 1
        mouseY = (mouseY - (mouseY % CELL_SIZE)) / CELL_SIZE
        newState[(mouseX + (mouseY * COLS))] = 1
    end
end



function checkForResetKeyPress()
    if love.keyboard.isDown({'r'}) then
        youWon = false
        for i=1, cellCount, 1 do
            newState[i] = math.floor(math.random() + 0.5)
        end
    end
end



function displayWinMessage()
    newState = getWinScreen()
    icolor = (icolor - 1) % 12
    for i=1, cellCount, 1 do
        col = ((i - 1) % COLS)
        row = ((i - 1 - col) / ROWS) * WINDOW_HEIGHT / WINDOW_WIDTH
        newcolindex = ((col + row + icolor) % 12) 
        setgColor(newcolindex)
        if newState[i] == 1 then
            grid[i]:revive( redG, greenG, blueG )
        else
            grid[i]:die()
        end
    end
end



function incrementgColor(add)
    icolor = icolor + add
    if icolor > 12 then icolor = 1 end
    if icolor < 1 then icolor = 12 end
    setColorWith24BitVal(colorlist[icolor])
end



function setgColor(setting)
    if setting > 12 then setting = 1 end
    if setting < 1 then setting = 12 end
    setColorWith24BitVal(colorlist[setting])
end



